/**
 * Quick email-based lookup set (lowercased) for O(1) checking during signup.
 */
export const WAITLIST_EMAILS = new Set(
  WAITLIST_DATA.map((entry) => entry.email.toLowerCase()),
);

/**
 * Check if an email belongs to a waitlist user.
 */
export function isWaitlistEmail(email: string): boolean {
  return WAITLIST_EMAILS.has(email.trim().toLowerCase());
}

/**
 * Get waitlist entry details by email.
 */
export function getWaitlistEntry(email: string): WaitlistEntry | undefined {
  const normalised = email.trim().toLowerCase();
  return WAITLIST_DATA.find((entry) => entry.email.toLowerCase() === normalised);
}
